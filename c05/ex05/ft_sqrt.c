int	ft_sqrt(int nb)
{
	int	num;

	num = 1;
	if (nb <= 0)
		return (0);
	while (num <= (nb / num) && num <= 46341)
	{
		if (num * num == nb)
			return (num);
		num++;
	}
	return (0);
}
