#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	module;

	module = '0';
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			nb *= -1;
			write(1, "-", 1);
		}
		if (nb > 9)
			ft_putnbr(nb / 10);
		module = nb % 10 + '0';
		write(1, &module, 1);
	}
}
