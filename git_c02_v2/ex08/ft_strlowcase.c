char	*ft_strlowcase(char *str)
{
	char	*inicio;

	inicio = str;
	while (*str != '\0')
	{
		if (*str >= 65 && *str <= 90)
		{
			*str += 32;
		}
		str++;
	}
	str = inicio;
	return (str);
}
